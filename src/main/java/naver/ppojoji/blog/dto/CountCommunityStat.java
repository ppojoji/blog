package naver.ppojoji.blog.dto;

public class CountCommunityStat {
	String range; 
	Integer postCnt;
	Integer delCnt;
	
	public String getRange() {
		return range;
	}
	public void setRange(String range) {
		this.range = range;
	}
	public Integer getPostCnt() {
		return postCnt;
	}
	public void setPostCnt(Integer postCnt) {
		this.postCnt = postCnt;
	}
	public Integer getDelCnt() {
		return delCnt;
	}
	public void setDelCnt(Integer delCnt) {
		this.delCnt = delCnt;
	}
	
	
}
